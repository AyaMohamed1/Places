$.each($cards, function (idx, value) {
  $intialCardId = "card-" + (idx + 1);
  $("#img-" + $intialCardId).attr("src", value.img);
  $("#title-" + $intialCardId).text(value.title);
  $("#prag-" + $intialCardId).text(value.prg);
  $("#date-" + $intialCardId).text(value.date);
  $("#com-" + $intialCardId).text(value.comments);
});

$(document).ready(function () {
  $(".card").click(function () {
    $cardId = $(this).attr("id");
    $cardNum = $cardId.replace("card-", "") - 1;
    $cardModalId = "#modal-img-" + $cardId;
    $cardImgId = "#img-" + $cardId;
    $cardTitleId = "#title-" + $cardId;
    $cardImg = $($cardImgId).attr("src");
    $cardTitle = $($cardTitleId).text();

    $("#modal-img-" + $cardId).attr("src", $cardImg);
    $("#modal-title-" + $cardId).text($cardTitle);
    $(".modal-body-" + $cardId).text($cards[$cardNum].prgFull);
  });
});
